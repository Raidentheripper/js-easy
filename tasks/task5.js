// Оголошення функції countOccurrences, яка приймає два аргументи:
// 'str' – рядок, в якому шукаємо, і 'char' – символ, який шукаємо.
function countOccurrences(str, char) {
  // 1. str.split(char):
  //    Метод 'split' розбиває рядок 'str' на масив підрядків,
  //    використовуючи 'char' як роздільник.
  // 2. .length:
  //    Ми отримуємо довжину (кількість елементів) масиву, отриманого після 'split'.
  //    (Наприклад, ["b", "n", "n", ""].length -> 4)
  // 3. - 1:
  //    Кількість частин, на які ділиться рядок, *завжди* на одиницю більша,
  //    ніж кількість роздільників (шуканого символу).
  //    (Наприклад, 4 частини мінус 1 -> 3 входження 'a')
  //    Тому ми віднімаємо 1, щоб отримати точну кількість входжень 'char'.
  return str.split(char).length - 1;
}

// Приклад виклику: "hello world".split('o') -> ["hell", " w", "rld"] (довжина 3). 3 - 1 = 2
console.log(countOccurrences("hello world", 'o')); // 2
// Приклад виклику: "banana".split('a') -> ["b", "n", "n", ""] (довжина 4). 4 - 1 = 3
console.log(countOccurrences("banana", 'a'));      // 3
module.exports = countOccurrences;
